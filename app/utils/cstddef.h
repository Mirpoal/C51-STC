#ifndef __CSTDDEF_H_
#define __CSTDDEF_H_

/**
* redefine the basic data type
*/
typedef char                s8;
typedef unsigned char       u8;
typedef short               s16;
typedef unsigned short      u16;
typedef int                 s32;
typedef unsigned    int     u32;
typedef float               f32;
typedef signed long long    s64;
typedef unsigned long long  u64;

#endif
